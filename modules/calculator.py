def add(x, y):
    number1 = int(x)
    number2 = int(y)
    return str(number1+number2)

def substract(x, y):
    number1 = int(x)
    number2 = int(y)
    return str(number1-number2)

def multiply(x, y):
    number1 = int(x)
    number2 = int(y)
    return str(number1*number2)

def divide(x, y):
    number1 = int(x)
    number2 = int(y)
    return str(number1/number2)
